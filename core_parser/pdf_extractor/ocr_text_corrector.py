    def _contextual_char_replacement(self, text: str) -> str:
        """
        Контекстная замена символов.
        
        Заменяет латинские буквы на кириллические в контексте русских слов.
        Улучшенная версия с более агрессивной заменой.
        """
        # Разбиваем на слова
        words = re.findall(r'\b\w+\b|\W+', text)
        result = []
        
        for word in words:
            if not re.match(r'\w+', word):
                result.append(word)
                continue
            
            # Проверяем, содержит ли слово кириллицу
            has_cyrillic = bool(re.search(r'[А-Яа-яЁё]', word))
            
            if has_cyrillic:
                # Заменяем латинские буквы на кириллические
                corrected_word = word
                for lat, cyr in self.char_replacements.items():
                    if lat != cyr:  # Пропускаем одинаковые
                        corrected_word = corrected_word.replace(lat, cyr)
                result.append(corrected_word)
            else:
                # Если нет кириллицы, проверяем, может ли это быть русское слово с ошибками
                # Если слово содержит смесь латиницы и похожих символов - пробуем заменить
                if len(word) > 2 and any(c in 'AaOoEeCcTtPpHhKkXxYyBbMm' for c in word):
                    # Возможно это русское слово с ошибками OCR
                    corrected_word = word
                    for lat, cyr in self.char_replacements.items():
                        if lat != cyr:
                            corrected_word = corrected_word.replace(lat, cyr)
                    # Если после замены появилась кириллица - используем исправленное слово
                    if re.search(r'[А-Яа-яЁё]', corrected_word):
                        result.append(corrected_word)
                    else:
                        result.append(word)
                else:
                    # Если нет кириллицы, возможно это число или английское слово
                    # Оставляем как есть, но исправляем похожие символы
                    corrected_word = word
                    # Заменяем только явно похожие символы
                    if re.match(r'^[0-9OIl1|]+$', word):
                        # Похоже на число с ошибками
                        corrected_word = word.replace('O', '0').replace('о', '0')
                        corrected_word = corrected_word.replace('I', '1').replace('l', '1').replace('|', '1')
                    result.append(corrected_word)
        
        return ''.join(result)
    
    def _correct_words(self, text: str) -> str:
        """
        Исправляет слова по словарю типичных ошибок.
        
        Использует нечеткое сравнение для поиска похожих слов.
        """
        words = text.split()
        corrected_words = []
        
        for word in words:
            # Убираем знаки препинания для сравнения
            word_clean = re.sub(r'[^\w]', '', word.lower())
            
            # Ищем похожие слова в словаре
            best_match = None
            best_similarity = 0
            
            for correct_word in self.word_corrections.keys():
                similarity = self._word_similarity(word_clean, correct_word)
                if similarity > best_similarity and similarity > 0.7:
                    best_similarity = similarity
                    best_match = correct_word
            
            if best_match:
                # Сохраняем регистр и знаки препинания
                if word.isupper():
                    corrected_word = best_match.upper()
                elif word[0].isupper():
                    corrected_word = best_match.capitalize()
                else:
                    corrected_word = best_match
                
                # Восстанавливаем знаки препинания
                punctuation = re.findall(r'[^\w]', word)
                if punctuation:
                    corrected_word += ''.join(punctuation)
                
                corrected_words.append(corrected_word)
            else:
                corrected_words.append(word)
        
        return ' '.join(corrected_words)
    
    def _word_similarity(self, word1: str, word2: str) -> float:
        """
        Вычисляет схожесть двух слов (простая метрика Левенштейна).
        
        Returns:
            Коэффициент схожести от 0 до 1
        """
        if not word1 or not word2:
            return 0.0
        
        if word1 == word2:
            return 1.0
        
        # Простая метрика на основе общих символов
        set1 = set(word1)
        set2 = set(word2)
        
        intersection = len(set1 & set2)
        union = len(set1 | set2)
        
        if union == 0:
            return 0.0
        
        jaccard = intersection / union
        
        # Учитываем длину
        len_diff = abs(len(word1) - len(word2))
        max_len = max(len(word1), len(word2))
        len_penalty = 1.0 - (len_diff / max_len) if max_len > 0 else 0.0
        
        return (jaccard * 0.7 + len_penalty * 0.3)
    
    def _final_normalization(self, text: str) -> str:
        """
        Финальная нормализация текста.
        
        Убирает лишние пробелы, нормализует кавычки и тире.
        """
        # Нормализация пробелов
        text = re.sub(r'\s+', ' ', text)
        text = re.sub(r'\s+([.,;:!?])', r'\1', text)  # Пробелы перед знаками препинания
        
        # Нормализация кавычек
        text = text.replace('«', '"').replace('»', '"')
        text = text.replace('"', '"').replace('"', '"')
        text = text.replace('„', '"').replace('"', '"')
        
        # Нормализация тире
        text = text.replace('−', '-').replace('–', '-').replace('—', '-')
        
        return text.strip()

